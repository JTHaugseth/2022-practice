@SuppressWarnings("ALL")

public class Wrapper_Classes {

    public static void main (String[] args) {

        // Wrapper class = Provides a way to use primitive data types as reference data types (eks. String)
        //                 reference data types contain useful methods
        //                 can be used with collections (ex.ArrayList)

        // primitive        // wrapper
        //----------        //---------
        // boolean          // Boolean
        // char             // Character
        // int              // Integer
        // double           // Double

        // autoboxing = the automatic conversion that the Java compiler makes between the primitive object and their corresponding object wrapper classes.
        // unboxing = the reverse of autoboxing. Automatic conversion of wrapper class to primitive.

        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "Bro";
    }
}
